package sec01.exam01;

import org.w3c.dom.ls.LSOutput;

public class VariableInitializationExample {

    public static void main(String[] args) {

        //변수 value 선언
        int value = 20;

        //변수 value 값을 읽어서 10을 더하는 산술 연산을 수행
        //연산의 결과 값을 변수 result의 초기값에 저장
        int result = value + 10;

        //변수 result 값을 일고 콘솔 출력
        System.out.println(result); //sout -> Live Template

    }
}