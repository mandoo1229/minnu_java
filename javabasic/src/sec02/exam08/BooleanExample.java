package sec02.exam08;

public class BooleanExample {
    public static void main(String[] args) {
        boolean stop = true; //python은 True라고 썻는데. java는 true이렇게 소문자로 쓴다.
        if (stop) {
            System.out.println("중지합니다.");
        } else {
            System.out.println("시작합니다.");
        }
    }
}
